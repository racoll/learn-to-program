current_number = beginning_number

while current_number > 2
  puts "#{current_number.to_s} bottles of beer on the wall, #{current_number.to_s} bottles of beer!"
      current_number = current_number - 1

  puts "Take one down, pass it around, #{current_number.to_s} bottles of beer on the wall!"
end

